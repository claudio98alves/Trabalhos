    /**
    * Esta ação serve para o utilizador entrar no painel em que se vai inscrever em locais pela primeira vez.
    * O utilizador só avança para o próximo painel, se tiver inserido o seu id e a password corretamente e se ainda não se tiver registado anteriormente.
    */
    private void registarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registarActionPerformed
        // TODO add your handling code here:
        int ver = 0;
        for(int i=0;i<comunidade.getListaElementos().size();i++){
            if(jTextField1.getText().equals(String.valueOf(comunidade.getListaElementos().get(i).getId()))){
                if(jPasswordField1.getText().equals((comunidade.getListaElementos().get(i).getPassword()))){
                    for(int j=0;j<comunidade.getListaInscritos().size();j++){
                        if(comunidade.getListaInscritos().get(j).getId()==comunidade.getListaElementos().get(i).getId()){
                            ver=-1;
                        }
                    }
                    if(ver!=-1){
                        jPasswordField1.setText("");
                        jTextField1.setText("");
                        this.I.getContentPane().removeAll();
                        this.I.getContentPane().add(new Inscrever(this.comunidade,this.locais,i,jPainelPrincipal,this.I,this));
                        this.I.revalidate();
                        ver = 1;
                    }
                }
            }
        }
        if(ver==0){
            JOptionPane.showMessageDialog(this, "Número de estudante ou palavra-chave inválidos!");
            jPasswordField1.setText("");
            jTextField1.setText("");
        }
        else if(ver==-1){
            JOptionPane.showMessageDialog(this, "Já se encontra registado, efetue login se o pretender.");
            jPasswordField1.setText("");
            jTextField1.setText("");
        }
        
    }//GEN-LAST:event_registarActionPerformed
