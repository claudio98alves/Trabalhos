    /**
    * Esta ação serve para o utilizador poder entrar na sua conta e poder adicionar locais à sua inscrição.
    * O utilizador só avança para o próximo painel, se tiver inserido o seu id e a password corretamente e se já estiver registado.
    */
    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        int ver = 0;
        for(int i=0;i<comunidade.getListaInscritos().size();i++){
            if(jTextField1.getText().equals(String.valueOf(comunidade.getListaInscritos().get(i).getId()))){
                ver=1;
            }
        }
        if(ver==1){
            for(int i=0;i<comunidade.getListaInscritos().size();i++){
                if(jTextField1.getText().equals(String.valueOf(comunidade.getListaInscritos().get(i).getId()))){
                    ver=-1;
                    if(jPasswordField1.getText().equals((comunidade.getListaInscritos().get(i).getPassword()))){
                            ver=1;
                            jPasswordField1.setText("");
                            jTextField1.setText("");
                            this.I.getContentPane().removeAll();
                            this.I.getContentPane().add(new AlterarLocais(this.comunidade,this.locais,i,jPainelPrincipal,this,this.I));
                            this.I.revalidate();
                            
                    }
                }
            }
        }
        else if(ver==0){
            JOptionPane.showMessageDialog(this, "Não se encontra registado.");
            jPasswordField1.setText("");
            jTextField1.setText("");
        }
        else if(ver==-1){
            JOptionPane.showMessageDialog(this, "Número de estudante ou palavra-chave inválidos.");
            jPasswordField1.setText("");
            jTextField1.setText("");
        }
    }//GEN-LAST:event_loginActionPerformed


